def add(a,b):
    result=a+b
    print("Sum:",result)
def subtract(a,b):
    result=a-b
    print("Difference:",result)
def multiply(a,b):
    result=a*b
    print("Multiplication:",result)
def divide(a,b):
    result=a/b
    print("Division:",result)

add(10,20)
subtract(10,20)
multiply(10,20)
divide(100,20)